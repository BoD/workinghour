package org.jraf.workinghour.repository

import co.touchlab.sqliter.DatabaseConfiguration
import com.squareup.sqldelight.db.SqlDriver
import com.squareup.sqldelight.drivers.native.NativeSqliteDriver
import com.squareup.sqldelight.drivers.native.wrapConnection

actual fun getSqliteDriver(schema: SqlDriver.Schema, databasePath: String): SqlDriver {
    val splitDatabasePath = splitPath(databasePath)
    println(splitDatabasePath.first)
    println(splitDatabasePath.second)
    return NativeSqliteDriver(
        configuration = DatabaseConfiguration(
            basePath = splitDatabasePath.first,
            name = splitDatabasePath.second,
            version = schema.version,
            create = { connection ->
                wrapConnection(connection) { schema.create(it) }
            },
            upgrade = { connection, oldVersion, newVersion ->
                wrapConnection(connection) { schema.migrate(it, oldVersion, newVersion) }
            }
        )
    )
}

private fun splitPath(path: String): Pair<String, String> {
    return Pair(
        path.substringBeforeLast("/"),
        path.substringAfterLast("/")
    )
}
